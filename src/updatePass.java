    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Update password:
        int Id=Integer.parseInt(jTextField1.getText());
        String old = jTextField3.getText();

        String pass1 = jPasswordField1.getText();
        String pass2 = jPasswordField2.getText();

        Transaction tx = null;
        try{
            Session se = NewHibernateUtil.getSessionFactory().getCurrentSession();
            tx = se.beginTransaction();
            LoginUser loginuser = (LoginUser) se.get(LoginUser.class, Id);
            
            String op = loginuser.getPassword();

            //update details
            
            if(old == null ? op == null : old.equals(op)){
                if(pass1 == null ? pass2 == null : pass1.equals(pass2)){
                    loginuser.setPassword(pass2);
                    se.saveOrUpdate(loginuser);
                    
                    JOptionPane.showMessageDialog(null,"Successfully Updated");
                    
                } else{
                    JOptionPane.showMessageDialog(null,"New password does not match");
                }
            }
            else{
                JOptionPane.showMessageDialog(null,"Old Password is wrong");
            }

            tx.commit();

            
            setVisible(false);
            new updatePass().setVisible(true);
        }
        catch(Exception e)
        {  if (tx != null) {
            tx.rollback();
        }
        e.printStackTrace();

        JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

