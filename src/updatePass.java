    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // Search Cashier:
        int Id = Integer.parseInt(jTextField1.getText());
        Transaction tx = null;
        try{

            Session se = NewHibernateUtil.getSessionFactory().getCurrentSession();
            tx = se.beginTransaction();
            LoginUser loginuser = (LoginUser) se.get(LoginUser.class, Id);

            if(tx!=null){
                jTextField2.setText(loginuser.getName());
                jTextField1.setEditable(false);
            }
            else
            JOptionPane.showMessageDialog(null, "Check your ID");
            tx.commit();
        }
        catch(Exception e)
        {  if (tx != null) {
            tx.rollback();
        }
        e.printStackTrace();

        JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

